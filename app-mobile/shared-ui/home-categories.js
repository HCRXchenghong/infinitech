export const HOME_CATEGORY_ORDER = CATEGORIES.map((item) => item.name)

export const DYNAMIC_HOME_CATEGORY_NAMES = ['美食', '团购', '甜点饮品', '超市便利']

const CATEGORY_DISPLAY_MAP = CATEGORIES.reduce((result, item) => {
  result[item.name] = item
  if (item.key) {
    result[item.key] = item
  }
  return result
}, {})

CATEGORY_DISPLAY_MAP['休闲玩乐'] = CATEGORY_DISPLAY_MAP.leisure_entertainment

function buildEmojiCategoryIcon(emoji = '🍽', background = '#F3F4F6') {
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 48 48"><circle cx="24" cy="24" r="22" fill="${background}"/><text x="24" y="32" font-size="26" text-anchor="middle">${emoji}</text></svg>`
  return `data:image/svg+xml;charset=utf-8,${encodeURIComponent(svg)}`
}

function normalizeRemoteCategory(raw = {}) {
  const routeType = String(raw.routeType || raw.route_type || '').trim()
  const routeValue = String(raw.routeValue || raw.route_value || '').trim()
  const key = String(raw.key || routeValue || raw.name || '').trim()
  const name = String(raw.name || raw.label || CATEGORY_DISPLAY_MAP[key]?.name || '').trim()
  if (!key && !name) return null

  const display = CATEGORY_DISPLAY_MAP[key] || CATEGORY_DISPLAY_MAP[name] || {}
  const bg = raw.bg || raw.bg_color || display.bg || '#F3F4F6'
  const iconType = String(raw.iconType || raw.icon_type || '').trim()
  const image = raw.image ||
    ((iconType === 'image' || iconType === 'external') && raw.icon
      ? raw.icon
      : display.image || buildEmojiCategoryIcon(raw.icon || raw.emoji || display.icon || '🍽', bg))

  return {
    ...display,
    ...raw,
    key: key || display.key || name,
    name,
    label: raw.label || name,
    bg,
    image,
    routeType: routeType || display.routeType || '',
    routeValue: routeValue || display.routeValue || '',
    badgeText: String(raw.badgeText || raw.badge_text || '').trim(),
    count: Number(raw.count || 0)
  }
}

export function buildHomeCategories(remoteCategories = []) {
  const remoteList = Array.isArray(remoteCategories) ? remoteCategories : []
  const remoteMap = new Map()

  remoteList.forEach((item) => {
    const normalized = normalizeRemoteCategory(item)
    if (normalized) {
      remoteMap.set(normalized.name, normalized)
    }
  })

  const ordered = HOME_CATEGORY_ORDER
    .map((name) => remoteMap.get(name) || CATEGORY_DISPLAY_MAP[name])
    .filter(Boolean)
    .map((item) => ({
      ...item,
      count: Number(item.count || 0)
    }))

  const extras = remoteList
    .map((item) => normalizeRemoteCategory(item))
    .filter((item) => item && !CATEGORY_DISPLAY_MAP[item.name])

  return [...ordered, ...extras]
}
